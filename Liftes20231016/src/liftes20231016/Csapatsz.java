public class Csapatsz {
    private int ora;
    private int perc;
    private int masodperc;
    private int sorszam;
    private int induloSzint;
    private int celSzint;

 
    public int Csapatsz(int ora, int perc, int masodperc, int sorszam, int induloSzint, int celSzint) {
        this.ora = ora;
        this.perc = perc;
        this.masodperc = masodperc;
        this.sorszam = sorszam;
        this.induloSzint = induloSzint;
        this.celSzint = celSzint;
    }
    
    

    public int getOra() {
        return ora;
    }

    public int getPerc() {
        return perc;
    }

    public int getMasodperc() {
        return masodperc;
    }

    public int getSorszam() {
        return sorszam;
    }

    public int getInduloSzint() {
        return induloSzint;
    }

    public int getCelSzint() {
        return celSzint;
    }

    public void setOra(int ora) {
        this.ora = ora;
    }

    public void setPerc(int perc) {
        this.perc = perc;
    }

    public void setMasodperc(int masodperc) {
        this.masodperc = masodperc;
    }

    public void setSorszam(int sorszam) {
        this.sorszam = sorszam;
    }

    public void setInduloSzint(int induloSzint) {
        this.induloSzint = induloSzint;
    }

    public void setCelSzint(int celSzint) {
        this.celSzint = celSzint;
    }
    
    
    
    
    
    
}
